const extendRankings = (base: Player[]): Player[] => {
  const targetCount = 1000;
  if (base.length >= targetCount) {
    return base;
  }

  const extra: Player[] = [];
  while (base.length + extra.length < targetCount) {
    const source = base[extra.length % base.length];
    const idNumber = base.length + extra.length + 1;
    extra.push({
      id: `u-${idNumber}`,
      name: source.name,
      score: source.score,
    });
  }

  return [...base, ...extra];
};

